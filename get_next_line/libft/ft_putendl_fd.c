#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int		a;

	a = 0;
	while (s[a])
	{
		ft_putchar_fd(s[a], fd);
		a++;
	}
	ft_putchar_fd('\n', fd);
}
