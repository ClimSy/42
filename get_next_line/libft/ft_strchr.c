#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	d;
	int		i;

	i = 0;
	d = c;
	while (s[i] != d)
	{
		if (!s[i])
			return (NULL);
		i++;
	}
	return ((char *)(s + i));
}
