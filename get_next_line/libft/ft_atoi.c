#include "libft.h"

int		ft_atoi(const char *c)
{
	int		n;
	int		r;

	n = 1;
	r = 0;
	while (*c == ' ' || *c == '\t' || *c == '\n' || *c == '\v'
				|| *c == '\r' || *c == '\f')
		c++;
	if (*c == '-')
	{
		n = -n;
		c++;
	}
	else if (*c == '+')
		c++;
	while (*c >= '0' && *c <= '9')
	{
		r *= 10;
		r += *c - '0';
		c++;
	}
	return (n * r);
}
